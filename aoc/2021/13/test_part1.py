from solution import fold


def test_fold():
    input_ = {
        (6, 10),
        (0, 14),
        (9, 10),
        (0, 3),
        (10, 4),
        (4, 11),
        (6, 0),
        (6, 12),
        (4, 1),
        (0, 13),
        (10, 12),
        (3, 4),
        (3, 0),
        (8, 4),
        (1, 10),
        (2, 14),
        (8, 10),
        (9, 0),
    }
    folds = [("y", 7)]
    expected = {
        (6, 4),
        (0, 0),
        (9, 4),
        (0, 3),
        (10, 4),
        (4, 3),
        (6, 0),
        (6, 2),
        (4, 1),
        (0, 1),
        (10, 2),
        (3, 4),
        (3, 0),
        (8, 4),
        (1, 4),
        (2, 0),
        (8, 4),
        (9, 0),
    }
    assert fold(input_, folds) == expected
